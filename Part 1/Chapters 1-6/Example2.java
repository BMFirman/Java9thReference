class Example2 {
	public static void main(String[] args) {
		// This declares the variable num
		int num;

		// This assigns num the value 100
		num = 100;

		// This prints out the value of num
		System.out.println("This is num: " + num);
		// This multiplies num by 2
		num = num * 2;

		// This prints out the updated num value
		System.out.println("The value of num * 2 is: " + num);
	}
}